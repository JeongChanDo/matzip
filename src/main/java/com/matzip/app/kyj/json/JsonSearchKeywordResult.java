package com.matzip.app.kyj.json;

public class JsonSearchKeywordResult {

	int no;
	String upsoName;
	String upsoAddr;
	String uptaeName; 
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getUpsoName() {
		return upsoName;
	}
	public void setUpsoName(String upsoName) {
		this.upsoName = upsoName;
	}
	public String getUpsoAddr() {
		return upsoAddr;
	}
	public void setUpsoAddr(String upsoAddr) {
		this.upsoAddr = upsoAddr;
	}
	public String getUptaeName() {
		return uptaeName;
	}
	public void setUptaeName(String uptaeName) {
		this.uptaeName = uptaeName;
	}
	
	
}
