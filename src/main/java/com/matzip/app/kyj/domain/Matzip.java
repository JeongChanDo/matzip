package com.matzip.app.kyj.domain;

public class Matzip {
	
	private int no;
	private String upso_code;
	private String upso_name;
	private String upso_addr;
	private String upso_tel;
	private String uptae_name;
	
	
	public Matzip() { }

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getUpso_code() {
		return upso_code;
	}

	public void setUpso_code(String upso_code) {
		this.upso_code = upso_code;
	}

	public String getUpso_name() {
		return upso_name;
	}

	public void setUpso_name(String upso_name) {
		this.upso_name = upso_name;
	}

	public String getUpso_addr() {
		return upso_addr;
	}

	public void setUpso_addr(String upso_addr) {
		this.upso_addr = upso_addr;
	}

	public String getUpso_tel() {
		return upso_tel;
	}

	public void setUpso_tel(String upso_tel) {
		this.upso_tel = upso_tel;
	}

	public String getUptae_name() {
		return uptae_name;
	}

	public void setUptae_name(String uptae_name) {
		this.uptae_name = uptae_name;
	}

}
